package flowacademy.settlersstrategy;

public class Soldiers {
    private int number;
    private int power;

    public Soldiers(int number, int power) {
        this.number = number;
        this.power = power;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }

}